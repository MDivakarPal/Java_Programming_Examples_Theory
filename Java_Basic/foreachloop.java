/*
Foreach loop :-
Foreach loop is use to access the collection.
Foreach can be use on a unknown size of collection.
Foreach loop is not applicable on single value variable.
*/
class Foreach
{
	public static void main(String [] a)
	{
		int al[]={4,4,74,4,4,4,};
		for (int k:al)
		{
			System.out.println(k);
		}
	}
}